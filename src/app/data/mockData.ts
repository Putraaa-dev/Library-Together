export type UserRole = 'admin' | 'petugas' | 'user';
export type LoanStatus = 'pending' | 'approved' | 'rejected' | 'returned' | 'overdue';

export interface User {
  id: string;
  name: string;
  email: string;
  password: string;
  role: UserRole;
  avatar: string;
  phone: string;
  address: string;
  createdAt: string;
  status: 'active' | 'inactive';
}

export interface Book {
  id: string;
  title: string;
  author: string;
  isbn: string;
  category: string;
  description: string;
  cover: string;
  publisher: string;
  year: number;
  stock: number;
  available: number;
  location: string;
  pages: number;
  language: string;
}

export interface Loan {
  id: string;
  userId: string;
  bookId: string;
  borrowDate: string;
  dueDate: string;
  returnDate: string | null;
  status: LoanStatus;
  notes: string;
}

